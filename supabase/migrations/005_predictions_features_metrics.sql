-- Production-grade integrity tables for measurable +EV validation.
-- Adds the critical predictions_log table plus internal features, optional bets,
-- and daily_metrics for SLO / CLV / ROI reporting.

CREATE TABLE IF NOT EXISTS public.features (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id TEXT NOT NULL REFERENCES public.games(game_id) ON DELETE CASCADE,
    feature_timestamp TIMESTAMPTZ NOT NULL,
    market_snapshot_id BIGINT REFERENCES public.odds_snapshots(id),
    feature_version TEXT NOT NULL DEFAULT 'v1',
    features JSONB NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (game_id, feature_timestamp, feature_version)
);

CREATE TABLE IF NOT EXISTS public.predictions_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id TEXT NOT NULL,
    model_version TEXT NOT NULL DEFAULT 'unknown',
    feature_version TEXT NOT NULL DEFAULT 'v1',

    prediction_timestamp TIMESTAMPTZ NOT NULL,
    request_received_at TIMESTAMPTZ NOT NULL,
    response_sent_at TIMESTAMPTZ,
    latency_ms INTEGER,

    feature_timestamp TIMESTAMPTZ,
    market_snapshot_id BIGINT REFERENCES public.odds_snapshots(id),
    market_snapshot_timestamp TIMESTAMPTZ NOT NULL,
    market_phase TEXT NOT NULL DEFAULT 'unknown'
        CHECK (market_phase IN ('opening', 'pre_lineup', 'post_lineup', 'closing', 'unknown')),

    closing_snapshot_id BIGINT REFERENCES public.odds_snapshots(id),
    closing_snapshot_timestamp TIMESTAMPTZ,
    result_finalized_at TIMESTAMPTZ,

    market_total NUMERIC NOT NULL,
    over_price INTEGER NOT NULL,
    under_price INTEGER NOT NULL,
    raw_model_total NUMERIC NOT NULL,
    calibrated_model_total NUMERIC NOT NULL,
    edge_runs NUMERIC NOT NULL,
    side TEXT NOT NULL CHECK (side IN ('OVER', 'UNDER', 'PASS')),
    should_bet BOOLEAN NOT NULL DEFAULT FALSE,
    estimated_probability NUMERIC NOT NULL,
    break_even_probability NUMERIC NOT NULL,
    expected_value NUMERIC NOT NULL,
    full_kelly_fraction NUMERIC,
    applied_kelly_fraction NUMERIC,
    stake NUMERIC NOT NULL DEFAULT 0,
    confidence TEXT NOT NULL CHECK (confidence IN ('LOW', 'MEDIUM', 'HIGH')),
    reason TEXT NOT NULL,

    closing_total NUMERIC,
    total_runs INTEGER,
    clv NUMERIC,
    pnl NUMERIC,
    roi NUMERIC,
    truth_status TEXT NOT NULL DEFAULT 'PENDING'
        CHECK (truth_status IN ('PENDING', 'READY', 'VOID')),

    calibration JSONB NOT NULL DEFAULT '{}'::jsonb,
    features JSONB NOT NULL DEFAULT '{}'::jsonb,
    payload JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT predictions_log_time_order CHECK (
        market_snapshot_timestamp <= prediction_timestamp
        AND (feature_timestamp IS NULL OR feature_timestamp <= prediction_timestamp)
        AND (closing_snapshot_timestamp IS NULL OR closing_snapshot_timestamp >= prediction_timestamp)
        AND (result_finalized_at IS NULL OR result_finalized_at >= prediction_timestamp)
    ),
    CONSTRAINT predictions_log_latency_slo CHECK (
        latency_ms IS NULL OR latency_ms >= 0
    )
);

CREATE TABLE IF NOT EXISTS public.bets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    prediction_id BIGINT NOT NULL REFERENCES public.predictions_log(id) ON DELETE CASCADE,
    game_id TEXT NOT NULL,
    side TEXT NOT NULL CHECK (side IN ('OVER', 'UNDER')),
    line NUMERIC NOT NULL,
    price INTEGER NOT NULL,
    stake NUMERIC NOT NULL,
    execution_status TEXT NOT NULL DEFAULT 'paper'
        CHECK (execution_status IN ('paper', 'queued', 'placed', 'rejected', 'cancelled')),
    placed_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.daily_metrics (
    metric_date DATE PRIMARY KEY,
    predictions INTEGER NOT NULL DEFAULT 0,
    bets INTEGER NOT NULL DEFAULT 0,
    avg_clv NUMERIC NOT NULL DEFAULT 0,
    clv_win_rate NUMERIC NOT NULL DEFAULT 0,
    roi NUMERIC NOT NULL DEFAULT 0,
    avg_latency_ms NUMERIC NOT NULL DEFAULT 0,
    p95_latency_ms NUMERIC NOT NULL DEFAULT 0,
    max_data_lag_seconds NUMERIC NOT NULL DEFAULT 0,
    freshness_slo_pass BOOLEAN NOT NULL DEFAULT FALSE,
    latency_slo_pass BOOLEAN NOT NULL DEFAULT FALSE,
    success_criteria_pass BOOLEAN NOT NULL DEFAULT FALSE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_features_game_time ON public.features(game_id, feature_timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_predictions_log_game_id ON public.predictions_log(game_id);
CREATE INDEX IF NOT EXISTS idx_predictions_log_prediction_timestamp ON public.predictions_log(prediction_timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_predictions_log_truth_status ON public.predictions_log(truth_status, updated_at DESC);
CREATE INDEX IF NOT EXISTS idx_predictions_log_should_bet ON public.predictions_log(should_bet, prediction_timestamp DESC);
CREATE INDEX IF NOT EXISTS idx_bets_prediction_id ON public.bets(prediction_id);
CREATE INDEX IF NOT EXISTS idx_bets_game_id ON public.bets(game_id);

